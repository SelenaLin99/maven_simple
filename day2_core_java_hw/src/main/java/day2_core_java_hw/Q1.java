package day2_core_java_hw;

public class Q1 {
	
	public static void main(String[] args) {
//		1) Take couple of variables (like int=1,J=5) and do
//			anthmetic
//			Unary
//			Relational and
//			Logical oPeration
		int i = 1;
		int j = 5;
		// Arithmetic
		System.out.println("add " + (i + j));
		System.out.println("subtract " + (i - j));
		System.out.println("multiply " + (i * j));
		System.out.println("divide " + (i / j));
		System.out.println("modulos  " + (i % j));
//		Unary
		System.out.println("post increments  " + (i++));
		System.out.println("pre increments  " + (++i));
		System.out.println("post decrements  " + (i--));
		System.out.println("pre decrements  " + (--i));
//  relational
		System.out.println("i == j " + (i == j));
		System.out.println("i != j " + (i != j));
		System.out.println("i > j " + (i > j));
		System.out.println("i >= j " + (i >= j));
		System.out.println("i < j " + (i < j));
		System.out.println("i <= j " + (i <= j));
		
// logical
		System.out.println("(i >= j) && (i < j) " + ((i >= j) && (i < j)));
		System.out.println("(i >= j) || (i < j) " + ((i >= j) || (i < j)));
		System.out.println("!(i >= j) " + (!(i >= j)));



		


		





		

	}

}
